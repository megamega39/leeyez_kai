        };

        private static string _currentLang = "ja";
        private static Dictionary<string, string>? _strings;

        public static string CurrentLanguage => _currentLang;

        public static void SetLanguage(string code)
        {
            _currentLang = code;
            _strings = code switch
            {
                "en" => En,
                "zh-CN" => ZhCN,
                "zh-TW" => ZhTW,
                "ko" => Ko,
                "es" => Es,
                "pt-BR" => PtBR,
                "fr" => Fr,
                "de" => De,
                "ru" => Ru,
                "vi" => Vi,
                "th" => Th,
                _ => Ja
            };
        }

        public static string Get(string key)
        {
            var strings = _strings ?? Ja;
            return strings.TryGetValue(key, out var val) ? val : (Ja.TryGetValue(key, out var ja) ? ja : key);
        }

